package methods;

public class _MethodsInfo_ {
    public static void main(String[] args) {
        /*
        What do we need to create a method:
        1- A proper name
        2- Decide its accessibility level (public or private)
        3- Decide its accessibility path (You want to call it with class name or with object name)
                    - static -> to call use class name
                    - non-static -> to call use object name
        4- Decide if it returns something or not
                    - if it returns -> then decide what it returns
                    - if it does not return -> use void
        5- Does it take any arguments
                    - if it takes, then decide what arguments it will take and how many

        NOTE: if your method is not void, you MUST use return keyword in the body
        NOTE: if your method returns an int, then the variable used MUST be an int
        NOTE: Every method must have a body that runs a task
        NOTE: We can have multiple methods sharing the same name in the same class only if they have different arguments.
                Different arguments could be either the number of arguments or the types of arguments
        NOTE: Never create a method inside another method
        */
    }
}
