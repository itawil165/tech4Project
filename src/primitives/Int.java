package primitives;

public class Int {
    public static void main(String[] args){
            // dataType variable = data;
        int x = 5;
        int y = 2;
        System.out.println(y - x);

        int maxInt = Integer.MAX_VALUE; // 0 and 1 to 2,147,483,647 (~2 million bits)
        int minInt = Integer.MIN_VALUE; // -2,147,483,648 to -1 (~2 million bits)
        System.out.println(maxInt);
        System.out.println(minInt);
            }
}
