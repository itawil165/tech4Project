package variables;

public class DeclarationsANDAssignments {
    public static void main(String[] args) {

        int myFirstInt; // Single declarations
        int mySecondInt;
        double myFirstDouble;
        double mySecondDouble;

        int myInt1, myInt2, myInt3; // Multiple declarations
        double myDbl1, myDbl2, myDbl3;

        myFirstInt = 2; // Single assignments
        mySecondInt = 5;
        myFirstDouble = 3.54;
        mySecondDouble = 9.431;

        int myInt11 = 2, myInt22 = 5, myInt33 = 15; // Multiple assignments
        double myDbl11 = 3.54, myDbl22 = 9.431, myDbl33 = 15.254;

        System.out.println(myInt11 + "\n" + myInt22 + "\n" + myInt33 + "\n" + myDbl11 + "\n" + myDbl22 + "\n" + myDbl33);

            }
}
